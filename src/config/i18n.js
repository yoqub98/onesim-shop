// Fixed getTranslation function with proper error handling
export const getTranslation = (lang, key) => {
  try {
    // Validate inputs
    if (!lang || !key) {
      console.warn('getTranslation: Missing lang or key parameter');
      return key || '';
    }

    // Get the language translations, fallback to default if not found
    const langTranslations = TRANSLATIONS[lang] || TRANSLATIONS[DEFAULT_LANGUAGE];
    
    if (!langTranslations) {
      console.warn(`getTranslation: No translations found for language "${lang}"`);
      return key;
    }

    // Split the key and traverse the object
    const keys = key.split('.');
    let value = langTranslations;
    
    for (const k of keys) {
      // Check if the current value is an object and has the key
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        // Key not found, return the original key
        console.warn(`getTranslation: Translation key "${key}" not found for language "${lang}"`);
        return key;
      }
    }
    
    // Make sure we return a string
    if (typeof value === 'string') {
      return value;
    } else {
      console.warn(`getTranslation: Translation for "${key}" is not a string:`, value);
      return key;
    }
  } catch (error) {
    console.error('getTranslation error:', error);
    return key || '';
  }
};

export const getCountryName = (countryCode, lang = DEFAULT_LANGUAGE) => {
  try {
    // Validate inputs
    if (!countryCode) {
      console.warn('getCountryName: Missing countryCode parameter');
      return '';
    }

    // Check if this is a European multi-country plan (EU-30, EU-15, etc.)
    if (countryCode.startsWith('EU-')) {
      const langCountries = COUNTRY_TRANSLATIONS[lang] || COUNTRY_TRANSLATIONS[DEFAULT_LANGUAGE];
      return langCountries['EU'] || 'Europe';
    }

    // Get the language-specific country translations
    const langCountries = COUNTRY_TRANSLATIONS[lang] || COUNTRY_TRANSLATIONS[DEFAULT_LANGUAGE];

    if (!langCountries) {
      console.warn(`getCountryName: No country translations found for language "${lang}"`);
      return countryCode;
    }

    // Get the country name from the current language
    let countryName = langCountries[countryCode];

    // If not found, fallback to English
    if (!countryName && COUNTRY_TRANSLATIONS['en']) {
      countryName = COUNTRY_TRANSLATIONS['en'][countryCode];
    }

    // If still not found, return the country code
    if (!countryName) {
      console.warn(`getCountryName: Country code "${countryCode}" not found for language "${lang}" or English fallback`);
      return countryCode;
    }

    return countryName;
  } catch (error) {
    console.error('getCountryName error:', error);
    return countryCode;
  }
};

// Helper function to get both translated and English names for search
export const getCountrySearchNames = (countryCode, lang = DEFAULT_LANGUAGE) => {
  const names = [];

  // Add translated name
  const langCountries = COUNTRY_TRANSLATIONS[lang] || COUNTRY_TRANSLATIONS[DEFAULT_LANGUAGE];
  if (langCountries && langCountries[countryCode]) {
    names.push(langCountries[countryCode]);
  }

  // Add English name
  if (COUNTRY_TRANSLATIONS['en'] && COUNTRY_TRANSLATIONS['en'][countryCode]) {
    const englishName = COUNTRY_TRANSLATIONS['en'][countryCode];
    if (!names.includes(englishName)) {
      names.push(englishName);
    }
  }

  // Add country code
  names.push(countryCode);

  return names;
};